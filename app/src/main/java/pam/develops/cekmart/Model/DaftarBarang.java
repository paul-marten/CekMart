public class DaftarBarang {
    public int id_daftarbarang;
    public String nama_barang;
    public String jenis_barang;
    public String nama_market;
    public String nama_lokasi;
    public String harga_barang;
    public String deskripsi;

    public int getId_daftarbarang() {
        return id_daftarbarang;
    }

    public void setId_daftarbarang(int id_daftarbarang) {
        this.id_daftarbarang = id_daftarbarang;
    }

    public String getNama_barang() {
        return nama_barang;
    }

    public void setNama_barang(String nama_barang) {
        this.nama_barang = nama_barang;
    }

    public String getJenis_barang() {
        return jenis_barang;
    }

    public void setJenis_barang(String jenis_barang) {
        this.jenis_barang = jenis_barang;
    }

    public String getNama_market() {
        return nama_market;
    }

    public void setNama_market(String nama_market) {
        this.nama_market = nama_market;
    }

    public String getNama_lokasi() {
        return nama_lokasi;
    }

    public void setNama_lokasi(String nama_lokasi) {
        this.nama_lokasi = nama_lokasi;
    }

    public String getHarga_barang() {
        return harga_barang;
    }

    public void setHarga_barang(String harga_barang) {
        this.harga_barang = harga_barang;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }
}
